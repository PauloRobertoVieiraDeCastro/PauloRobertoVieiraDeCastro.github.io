function clicar_mvd100(){
	var x = document.getElementById('status_mvd100');
	if(x.value==0){
		x.style = "background: green; color: white; font-weight: bold";
	}
	if(x.value==1){
		x.style = "background: yellow; color: black; font-weight: bold";
	}
	if(x.value==2){
		x.style = "background: red; color: white; font-weight: bold";	
	}
}

